package modelo;

public class Usuario {
private String nome;
private long cpf;
private String tel;
private String cep;
private int nCasa;
public String getNome() {
	return nome;
}
public void setNome(String nome) {
	this.nome = nome;
}
public long getCpf() {
	return cpf;
}
public void setCpf(long cpf) {
	this.cpf = cpf;
}
public String getTel() {
	return tel;
}
public void setTel(String tel) {
	this.tel = tel;
}
public String getCep() {
	return cep;
}
public void setCep(String cep) {
	this.cep = cep;
}
public int getnCasa() {
	return nCasa;
}
public void setnCasa(int nCasa) {
	this.nCasa = nCasa;
}




}
